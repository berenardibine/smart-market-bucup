-- Create a Haversine distance function for nearby queries
CREATE OR REPLACE FUNCTION public.haversine_distance(
  lat1 numeric, lng1 numeric,
  lat2 numeric, lng2 numeric
) RETURNS numeric AS $$
  SELECT 6371 * acos(
    LEAST(1.0, GREATEST(-1.0,
      cos(radians(lat1)) * cos(radians(lat2)) *
      cos(radians(lng2) - radians(lng1)) +
      sin(radians(lat1)) * sin(radians(lat2))
    ))
  );
$$ LANGUAGE sql IMMUTABLE;

-- Create function to get nearby products using Haversine
CREATE OR REPLACE FUNCTION public.get_nearby_products(
  user_lat numeric,
  user_lng numeric,
  radius_km numeric DEFAULT 100,
  max_results integer DEFAULT 50
)
RETURNS TABLE (
  id uuid,
  title text,
  price numeric,
  images text[],
  category text,
  created_at timestamptz,
  views integer,
  likes integer,
  rental_unit text,
  sponsored boolean,
  product_type text,
  country text,
  currency_code text,
  currency_symbol text,
  is_negotiable boolean,
  admin_posted boolean,
  lat numeric,
  lng numeric,
  distance_km numeric,
  seller_id uuid,
  seller_name text,
  seller_image text
) AS $$
  SELECT 
    p.id,
    p.title,
    p.price,
    p.images,
    p.category,
    p.created_at,
    p.views,
    p.likes,
    p.rental_unit,
    p.sponsored,
    p.product_type,
    p.country,
    p.currency_code,
    p.currency_symbol,
    p.is_negotiable,
    p.admin_posted,
    p.lat,
    p.lng,
    public.haversine_distance(user_lat, user_lng, p.lat, p.lng) AS distance_km,
    pr.id AS seller_id,
    pr.full_name AS seller_name,
    pr.profile_image AS seller_image
  FROM public.products p
  LEFT JOIN public.profiles pr ON p.seller_id = pr.id
  WHERE p.status = 'active'
    AND p.lat IS NOT NULL 
    AND p.lng IS NOT NULL
    AND public.haversine_distance(user_lat, user_lng, p.lat, p.lng) <= radius_km
  ORDER BY 
    p.sponsored DESC NULLS LAST,
    public.haversine_distance(user_lat, user_lng, p.lat, p.lng) ASC
  LIMIT max_results;
$$ LANGUAGE sql STABLE;
